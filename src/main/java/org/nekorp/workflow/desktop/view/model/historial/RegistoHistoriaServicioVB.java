package org.nekorp.workflow.desktop.view.model.historial;

import java.util.Date;
import org.nekorp.workflow.desktop.view.model.currency.MonedaVB;

/**
 *
 */
public class RegistoHistoriaServicioVB {

    private String numeroServicio;
    private String descripcionServicio;
    private Date fechaInicio;
    private MonedaVB costo;

    public String getNumeroServicio() {
        return numeroServicio;
    }

    public void setNumeroServicio(String numeroServicio) {
        this.numeroServicio = numeroServicio;
    }

    public String getDescripcionServicio() {
        return descripcionServicio;
    }

    public void setDescripcionServicio(String descripcionServicio) {
        this.descripcionServicio = descripcionServicio;
    }

    public Date getFechaInicio() {
        return fechaInicio;
    }

    public void setFechaInicio(Date fechaInicio) {
        this.fechaInicio = fechaInicio;
    }

    public MonedaVB getCosto() {
        return costo;
    }

    public void setCosto(MonedaVB costo) {
        this.costo = costo;
    }
}
